import 'package:flutter/material.dart';
import 'package:rflutter_alert/rflutter_alert.dart';
import 'package:sistema_inventario/components/BouncyPageRoute.dart';
import 'package:sistema_inventario/http/login.dart';
import 'package:sistema_inventario/principal.dart';

class LoginScreen extends StatefulWidget {
  const LoginScreen({super.key, required this.controller});
  final PageController controller;
  @override
  State<LoginScreen> createState() => _LoginScreenState();
}

class _LoginScreenState extends State<LoginScreen> {
  final TextEditingController _emailController = TextEditingController();
  final TextEditingController _passController = TextEditingController();

  login(context) async {
    if(_emailController.text == "" || _passController.text == ""){
      Alert(
        type: AlertType.error,
        context: context,
        title: "Ingrese un usuario y contrase침a",
      ).show();
    }else{
      Login request = Login(_emailController.text, _passController.text);
      dynamic response = await request.loguear();
      if(response["success"] == 1){
        Navigator.push(
          context,
          BouncyPageRoute(widget: const Principal())
        );             
      }else{
        Alert(
          type: AlertType.error,
          context: context,
          title: response["mensaje"],
          buttons: [
            DialogButton(
              onPressed: () => Navigator.pop(context),
              color: const Color.fromRGBO(0, 179, 134, 1.0),
              child: const Text(
                "OK",
                style: TextStyle(color: Colors.white, fontSize: 20),
              ),
            ),
          ],
        ).show();
      }  
    }
  }

